curso = "  Python"

print(curso.upper())
print(curso.lower())

#deixa primeira letra em maiusculo
print(curso.title())

#remover espaços em branco
print(curso.strip())

#esquerda
print(curso.lstrip())
#direita
print(curso.rstrip())

#Centralização - numero de caracteres que vai ocupar e # que vai completar os espaços
print(curso.center(10,"#"))

#junção -- join - passa item a item e adiciona o caractere que eu passei
print(".".join(curso))

