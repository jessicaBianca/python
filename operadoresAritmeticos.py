# adição subtração multiplicação
numeroUm = 10
numeroDois = 2

#adição
resultado = numeroUm + numeroDois
print(resultado)

#subtração
resultado = numeroUm - numeroDois
print(resultado)

#multiplicação
resultado = numeroUm * numeroDois
print(resultado)

# divisão e divisão inteira
numeroDivUm = 12

#divisão
print(numeroDivUm / 3)


#divisão inteira (tira o float)
print(numeroDivUm // 2)


#Módulo e exponenciação

#Módulo = resto da divisão do número
print(numeroUm % 3)

#Exponenciação (potência) - 2**3 - 2 elevado a 3 = 2 * 2 * 2 = 8 
resultado = 2 ** 3
print(resultado)  # Vai exibir 8


#Precendência dos operadores


# A precedência dos operadores define a ordem em que as operações matemáticas
# são realizadas em uma expressão. No caso das operações matemáticas comuns,
# a ordem de precedência é a seguinte:

# Parênteses (): Operações dentro de parênteses são realizadas primeiro.
# Exponenciação ^ ou **: A exponenciação é realizada antes das multiplicações,
# divisões e adições.
# Multiplicação * e Divisão /: São realizadas da esquerda para a direita.
# Soma + e Subtração -: São realizadas por último, também da esquerda para a direita.

# A ordem de precedência pode ser lembrada com a sigla PEMDAS, que significa:

# P - Parênteses
# E - Exponenciação
# M - Multiplicação
# D - Divisão
# A - Adição
# S - Subtração
