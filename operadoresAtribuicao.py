#Operador de atribuição = usados para definir valor inicial ou sobreescrever valor de variavel

#Atribuição simples
idade = 17
print(idade)

#Atribuição com adição
saldo = 500
saldo += 200

print(saldo)

#outra forma de leitura 
total = 500
total = total + 200

print(total)

